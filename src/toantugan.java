public class toantugan {
    public static void main(String[] args) {
        // gán cộng
        int x = 8;
        x += 5;
        System.out.println("x = " + x);

        // gán trừ
        int x2 = 8;
        x2 -= 5;
        System.out.println("x2 = " + x2);

        // gán nhân
        int x3 = 8;
        x3 *= 5;
        System.out.println("x3 = " + x3);

        // gán chia
        int x4 = 8;
        x4 /= 5;
        System.out.println("x4 = " + x4);

        // gán chia lấy dư
        int x5 = 8;
        x5 %= 5;
        System.out.println("x5 = " + x5);

        // các phép so sánh
        int a = 7;
        int b = 9;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
