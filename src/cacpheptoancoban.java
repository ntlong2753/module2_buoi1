public class cacpheptoancoban {
    public static void main(String[] args) {
        int a = 11;
        int b = 5;
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        // Phép cộng
        System.out.println("a + b = " + (a + b));

        //gán biến
        int kq = a + b;
        System.out.println("a + b = " + kq);

        // Phép trừ
        System.out.println("a - b = " + (a - b));

        // Phép nhân
        System.out.println("a * b = " + (a * b));

        // Phép chia
        System.out.println("a / b = " + (a / b));
        System.out.println("a / b = " + ((double)a / b));

        // Phép chia lấy dư
        System.out.println("a % b = " + (a % b));

    }
}
